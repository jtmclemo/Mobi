function testReferencePopulationMetricsBasicCase(testCase)
    selectedPeptides = "PEP1";

    combinedTable = table( ...
        "PEP1", ...
        "HLA-A*01:01", ...
        'VariableNames', {'Peptide','MHC'});

    referenceTable = table( ...
        ["Pop1"; "Pop1"], ...
        ["A"; "A"], ...
        ["A*01:01"; "A*02:01"], ...
        [0.20; 0.80], ...
        [36; NaN], ...
        [100; 100], ...
        'VariableNames', { ...
            'Population', ...
            'Locus', ...
            'Allele', ...
            'AlleleFrequency', ...
            'IndividualsFrequency', ...
            'SampleSize'});

    metrics = Mobi_reference_population_metrics(selectedPeptides, combinedTable, referenceTable);

    verifyTrue(testCase, metrics.didEvaluate);
    verifyEqual(testCase, metrics.numSelectedPeptides, 1);
    verifyEqual(testCase, metrics.numHitHLAs, 1);
    verifyEqual(testCase, metrics.numReferencePopulations, 1);
    verifyEqual(testCase, metrics.coveragePercent, 36, 'AbsTol', 1e-10);
    verifyEqual(testCase, metrics.expectedHits, 0.36, 'AbsTol', 1e-10);
    verifyEqual(testCase, metrics.conditionalMeanHits, 1, 'AbsTol', 1e-10);
    verifyEqual(testCase, metrics.pc90, 0, 'AbsTol', 1e-10);
end


