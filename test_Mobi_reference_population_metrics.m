    verifyEqual(testCase, metrics.populationWeightMode, "sample_size");
end


function testHitCountModeMultiplicityVsBinary(testCase)
    selectedPeptides = ["PEP1"; "PEP2"];

    combinedTable = table( ...
        ["PEP1"; "PEP2"], ...
        ["A*01:01"; "A*01:01"], ...
        'VariableNames', {'Peptide','MHC'});

    referenceTable = table( ...
        "Pop1", ...
        "A", ...
        "A*01:01", ...
        0.5, ...
        NaN, ...
        100, ...
        'VariableNames', {'Population','Locus','Allele','AlleleFrequency','IndividualsFrequency','SampleSize'});

    metricsMultiplicity = Mobi_reference_population_metrics( ...
        selectedPeptides, combinedTable, referenceTable, struct('hitCountMode', 'multiplicity'));
    metricsBinary = Mobi_reference_population_metrics( ...
        selectedPeptides, combinedTable, referenceTable, struct('hitCountMode', 'binary_per_allele'));

    verifyGreaterThan(testCase, metricsMultiplicity.expectedHits, metricsBinary.expectedHits);
    verifyEqual(testCase, metricsBinary.expectedHits, 0.75, 'AbsTol', 1e-10);
    verifyEqual(testCase, metricsMultiplicity.expectedHits, 1.5, 'AbsTol', 1e-10);
end


